  <p>The Board and Management of <strong>The Compass</strong> are fully dedicated to upholding the confidentiality,
    integrity, and
    availability of all physical and electronic information assets in the organization. Our commitment is essential to
    safeguard our assets, adhere to legal and regulatory requirements, meet contractual obligations, and preserve our
    reputation.</p>

  <p>We recognize the significance of the Information Security Management System (ISMS) in accordance with ISO 27001 and
    the Quality Management System (QMS) aligned with ISO 9001. These integrated systems are instrumental in achieving
    our organizational objectives, facilitating secure information sharing, optimizing electronic operations, and
    effectively mitigating information and technology-related risks.
  </p>

  <p>
    <stong>Our Policy Ensures:</stong>
  </p>
  <ul>
    <li>The ISMS, complemented by the QMS, provides a strategic context for identifying, assessing, evaluating, and
      controlling information/process/service-related risks. It ensures the implementation and maintenance of robust
      controls aligned with our risk management strategy.</li>
    <li>Critical elements such as business continuity and contingency plans, data backup procedures, access controls to
      systems, and information security incident reporting are integral to this policy. All employees of <strong>The
        Compass</strong> bear
      the responsibility of promptly reporting incidents.</li>
    <li>Compliance with this policy is mandatory for all employees and external parties identified in the Integrated
      Management Systems. Adequate training will be provided to staff and certain external parties to ensure
      understanding and compliance.</li>
    <li>Continuous and systematic reviews of the ISMS and QMS will be conducted, to drive improvements, maintain
      effectiveness, and foster a culture of continual improvement.</li>
  </ul>
  <p>The management is firmly committed to sustaining the excellence of the ISMS and QMS in <strong>The
      Compass</strong>.</p>
  <p>Any breach of this policy, or security mechanisms may result in disciplinary measures; including termination of
    employment/contract, and legal action in accordance with the Cybercrime Prohibition Act 2015.</p>
  <p>This policy statement, reflects our organization's commitment to securing information, delivering high-quality
    services and continuous enhancement, tailored to the specific context of an IT GRC and Cybersecurity consulting
    firm. The integration of ISMS and QMS ensures an efficient and effective approach to risk management and fostering
    confidence among our clients and stakeholders.</p>
